// Api Error Handler 

class ApiError extends Error {
    constructor(
        statusCode,
        message = 'Some Thing Went Wrong Api Error',
        error = [],
        stack = ""
    ) {
        super(message)
        this.statusCode = statusCode
        this.data = null
        this.message = message
        this.success = false;
        this.error = error;

        if (stack) {
            this.stack = stack;
        } else {
            Error.captureStackTrace(this, this.constructor);
        }
    }
}
